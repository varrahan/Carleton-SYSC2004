
/**
 * Animal class from Final Exam Review Questions Part 1
 *
 * @author Lynn Marshall
 * @version 1.0
 */
public class Animal
{
    private String name;

    /**
     * Constructor for objects of class Animal
     * 
     * @param name The animal's name
     */
    public Animal(String name)
    {
        this.name = name;
    }

    /**
     * toString returns a string representation of the animal as:
     * <name>
     * 
     * @return the string representation of the animal
     */
    public String toString()
    {
        return name;
    }
}
